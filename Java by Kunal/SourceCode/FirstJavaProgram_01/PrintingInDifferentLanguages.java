package SourceCode.FirstJavaProgram_01;

public class PrintingInDifferentLanguages {
    public static void main(String[] args) {
        System.out.println("Printing \"Hello\" in English");
        System.out.println("Hello");
        System.out.println("Printing \"Hello\" in Tamil");
        System.out.println("வணக்கம்");
        System.out.println("Printing \"Hello\" in Chinese");
        System.out.println("你好");
        System.out.println("Printing \"Hello\" in Japanese");
        System.out.println("こんにちは");
    }
}
